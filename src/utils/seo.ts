export interface SEOData {
  title: string;
  description: string;
  keywords?: string;
  canonical?: string;
  ogTitle?: string;
  ogDescription?: string;
  ogImage?: string;
  ogType?: string;
  noindex?: boolean;
  nofollow?: boolean;
}

// Базовые SEO данные для всех страниц
const baseSEO = {
  siteName: 'НОРДИНЖИНИРИНГ',
  baseUrl: 'https://nordengineering.ru',
  defaultImage: 'https://nordengineering.ru/og-image.jpg',
  defaultKeywords: 'климатическое оборудование, вентиляция, кондиционирование, отопление, НОРДИНЖИНИРИНГ'
};

// Функция для создания SEO данных
export const createSEOData = (
  title: string,
  description: string,
  options: Partial<SEOData> = {}
): SEOData => {
  const fullTitle = title.includes(baseSEO.siteName) 
    ? title 
    : `${title} | ${baseSEO.siteName}`;

  return {
    title: fullTitle,
    description,
    keywords: options.keywords || baseSEO.defaultKeywords,
    canonical: options.canonical,
    ogTitle: options.ogTitle || fullTitle,
    ogDescription: options.ogDescription || description,
    ogImage: options.ogImage || baseSEO.defaultImage,
    ogType: options.ogType || 'website',
    noindex: options.noindex || false,
    nofollow: options.nofollow || false,
    ...options
  };
};

